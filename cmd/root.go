package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "slurp",
	Short: "Export data from or import data to a GotoSocial-compatible Fediverse instance",
}

// User is the username@domain of the account whose data we're working with.
// For the `bluesky download` command only, there is no username part and the leading @ should be omitted.
var User string

// File is the file path of the data we're working with.
// Applies to export/import commands only.
var File string

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

func init() {
	rootCmd.PersistentFlags().StringVarP(&User, "user", "u", "", "username@domain of the account whose data we're working with")
}
